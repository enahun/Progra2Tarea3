
public class Animal {
	//Atributos de la clase
	String nombre;
	int edad;
	//metodo getNombre retorna variable nombre
	String getNombre()
	{
		return nombre;
	}
	//metodo getEdad retorna variable edad
	int getEdad()
	{
		return edad;
	}
	//metodo setNombre asigna valor a variable nombre
	void setNombre(String nombre)
	{
		this.nombre=nombre;
	}
	//metodo setEdad asigna valor a variable edad
	void setEdad(int edad)
	{
		this.edad=edad;
	}

	

}
